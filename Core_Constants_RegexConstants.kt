package com.sentrix.core.constants

object RegexConstants {

    /* ============================================================
     * EMAIL VALIDATION
     * ============================================================ */

    const val EMAIL_REGEX =
        "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$"

    /* ============================================================
     * PASSWORD VALIDATION
     * ============================================================ */

    const val PASSWORD_REGEX =
        "^(?=.*[A-Z])(?=.*[a-z])(?=.*\\d)(?=.*[@#\$%^&+=!]).{8,64}$"

    /* ============================================================
     * USERNAME VALIDATION
     * ============================================================ */

    const val USERNAME_REGEX =
        "^[a-zA-Z0-9_]{4,20}$"

    /* ============================================================
     * PHONE NUMBER VALIDATION
     * ============================================================ */

    const val PHONE_REGEX =
        "^[+]?[0-9]{10,15}$"

    /* ============================================================
     * URL VALIDATION
     * ============================================================ */

    const val URL_REGEX =
        "^(https?|ftp)://[^\\s/$.?#].[^\\s]*$"

    /* ============================================================
     * IP ADDRESS VALIDATION
     * ============================================================ */

    const val IPV4_REGEX =
        "^((25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)(\\.|$)){4}$"

    const val IPV6_REGEX =
        "^(?:[\\da-fA-F]{1,4}:){7}[\\da-fA-F]{1,4}$"

    /* ============================================================
     * DOMAIN VALIDATION
     * ============================================================ */

    const val DOMAIN_REGEX =
        "^(?!-)[A-Za-z0-9-]{1,63}(?<!-)\\.[A-Za-z]{2,6}$"

    /* ============================================================
     * FILE VALIDATION
     * ============================================================ */

    const val APK_FILE_REGEX =
        "^.*\\.apk$"

    const val PDF_FILE_REGEX =
        "^.*\\.pdf$"

    const val JSON_FILE_REGEX =
        "^.*\\.json$"

    /* ============================================================
     * HASH VALIDATION
     * ============================================================ */

    const val MD5_REGEX =
        "^[a-fA-F0-9]{32}$"

    const val SHA256_REGEX =
        "^[a-fA-F0-9]{64}$"

    const val SHA512_REGEX =
        "^[a-fA-F0-9]{128}$"

    /* ============================================================
     * HEX COLOR VALIDATION
     * ============================================================ */

    const val HEX_COLOR_REGEX =
        "^#([A-Fa-f0-9]{6}|[A-Fa-f0-9]{8})$"

    /* ============================================================
     * DEVICE ID VALIDATION
     * ============================================================ */

    const val DEVICE_ID_REGEX =
        "^[A-Za-z0-9_-]{8,64}$"

    /* ============================================================
     * OTP VALIDATION
     * ============================================================ */

    const val OTP_REGEX =
        "^[0-9]{4,6}$"

    /* ============================================================
     * BIOMETRIC TOKEN VALIDATION
     * ============================================================ */

    const val BIOMETRIC_TOKEN_REGEX =
        "^[A-Za-z0-9+/=]{20,512}$"

    /* ============================================================
     * SQL INJECTION DETECTION
     * ============================================================ */

    const val SQL_INJECTION_REGEX =
        "(?i)(SELECT|INSERT|DELETE|UPDATE|DROP|ALTER|TRUNCATE|EXEC|UNION).*"

    /* ============================================================
     * XSS DETECTION
     * ============================================================ */

    const val XSS_REGEX =
        "(?i)<script.*?>.*?</script.*?>"

    /* ============================================================
     * CREDIT CARD VALIDATION
     * ============================================================ */

    const val CREDIT_CARD_REGEX =
        "^[0-9]{13,19}$"

    /* ============================================================
     * ZIP / POSTAL CODE VALIDATION
     * ============================================================ */

    const val ZIP_CODE_REGEX =
        "^[0-9]{4,10}$"

    /* ============================================================
     * DATE VALIDATION
     * ============================================================ */

    const val DATE_REGEX =
        "^\\d{2}/\\d{2}/\\d{4}$"

    /* ============================================================
     * TIME VALIDATION
     * ============================================================ */

    const val TIME_REGEX =
        "^([01]\\d|2[0-3]):([0-5]\\d):([0-5]\\d)$"

}
